//重定向c库函数printf到串口，重定向后可使用printf函数
int fputc(int ch,FILE *f)

{
	ch_t=ch;
    uint8_t temp[1]={ch_t};

    HAL_UART_Transmit(&huart1,temp,1,1000);        //UartHandle是串口的句柄
	return ch_t;

}

//重定向c库函数scanf到串口，重写向后可使用scanf、getchar等函数
int fgetc(FILE *f)
{

	HAL_UART_Receive (&huart1,&ch_r,1,0xffff);//接收
	return ch_r;
}

